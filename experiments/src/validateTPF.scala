package experiments

import io.github.quafadas.table.{*, given}
import vecxt.all.{*, given}
import io.github.quafadas.scautable.HeaderOptions

@main def validateTPF(): Unit =
  // 1. Load the reference data produced by MATLAB/Octave
  val X = CSV.resource("ref_X.csv", CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))
  val y = CSV.resource("ref_y.csv", CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))
  val Z = CSV.resource("ref_Z.csv", CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))

  println(X.data.printArr)

  val mX = Matrix(X.data, X.rows, X.cols, X.colStride, X.rowStride, 0)
  val mY = Matrix(y.data, y.rows, y.cols, y.colStride, y.rowStride, 0)
  val mZ = Matrix(Z.data, Z.rows, Z.cols, Z.colStride, Z.rowStride, 0)

  println(s"X shape: ${mX.rows} x ${mX.cols}")
  println(s"y shape: ${mY.rows} x ${mY.cols}")
  println(s"Z shape: ${mZ.rows} x ${mZ.cols}")

  // 2. Run the TPRF estimation (closed-form, no PLS, with proxies provided)
  val result = ThreePrf.TPRF(
    _X          = mX,
    y           = mY,
    _Z          = mZ,
    L           = 0,          // use the provided Z, don't auto-generate
    pls         = false,
    centerData  = false,
    scaleData   = true,
    closed_form = true,
    fitalg      = 2
  )

  println(s"Run complete.")
  println(s"R-squared: ${result.alpha_hat}")

  // 3. Save results
  def saveMatrix(m: Matrix[Double], path: os.Path): Unit =
    val rows = for i <- 0 until m.rows yield
      (for j <- 0 until m.cols yield m(i, j).toString).mkString(",")
    os.write.over(path, rows.mkString("\n") + "\n")

  val outDir = os.pwd / "experiments" / "resources"

  saveMatrix(result.y_hat,    outDir / "scala_yhat.csv")
  saveMatrix(result.alpha_hat, outDir / "scala_alpha.csv")

  val rSquared =
    val yMean = (0 until mY.rows).map(mY(_, 0)).sum / mY.rows
    val ssTot = (0 until mY.rows).map(i => math.pow(mY(i,0) - yMean, 2)).sum
    val ssRes = (0 until mY.rows).map(i => math.pow(mY(i,0) - result.y_hat(i,0), 2)).sum
    if ssTot == 0.0 then 0.0 else 1.0 - ssRes / ssTot

  saveMatrix(Matrix(Array(rSquared), 1, 1), outDir / "scala_rsquare.csv")
  println(s"R-squared: $rSquared")

  // 4. Compare against MATLAB (KP) reference outputs
  val kpYhat   = CSV.resource("kp_yhat.csv",   CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))
  val kpAlpha  = CSV.resource("kp_alpha.csv",  CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))
  val kpRsq    = CSV.resource("kp_rsquare.csv",CsvOpts(readAs = ReadAs.ArrayDenseColMajor[Double](), headerOptions = HeaderOptions.AutoGenerated))

  println(s"kpRsq: "  + kpRsq.data.printArr)

  val mKpYhat  = Matrix(kpYhat.data,  kpYhat.rows,  kpYhat.cols,  kpYhat.colStride,  kpYhat.rowStride,  0)
  val mKpAlpha = Matrix(kpAlpha.data, kpAlpha.rows, kpAlpha.cols, kpAlpha.colStride, kpAlpha.rowStride, 0)
  val kpRsqVal = kpRsq.data(0)

  println("\nVerifying against MATLAB (KP) references...")

  val yhatDiff  = (0 until mKpYhat.rows).map(i => math.abs(result.y_hat(i, 0)    - mKpYhat(i, 0))).fold(0.0)(math.max)
  val alphaDiff = (0 until mKpAlpha.rows).map(i => math.abs(result.alpha_hat(i, 0) - mKpAlpha(i, 0))).fold(0.0)(math.max)
  val rsqDiff   = math.abs(rSquared - kpRsqVal)

  val tol = 1e-10
  println(f"Yhat Max Diff:  $yhatDiff%.2e")
  println(f"Alpha Max Diff: $alphaDiff%.2e")
  println(f"RSq Diff:       $rsqDiff%.2e")

  if yhatDiff < tol && alphaDiff < tol && rsqDiff < tol then
    println("\nSUCCESS: Scala results match MATLAB (KP) references within tolerance.")
  else
    println("\nFAILURE: Scala results deviate from MATLAB (KP) references.")
    sys.exit(1)

  // 5. Run the NDArray version and compare against the Matrix version --------
  import vecxt.ndarray.NDArray
  import vecxt.ndarray.NDArray.*

  inline given ndBc: vecxt.BoundsCheck.BoundsCheck = vecxt.BoundsCheck.DoBoundsCheck.no

  def toNd(data: Array[Double], rows: Int, cols: Int): NDArray[Double] =
    NDArray[Double](data.clone(), Array(rows, cols))

  val ndX = toNd(X.data, X.rows, X.cols)
  val ndY = toNd(y.data, y.rows, y.cols)
  val ndZ = toNd(Z.data, Z.rows, Z.cols)

  println("\nRunning NDArray version...")
  val ndResult = ThreePrfNd.TPRF(
    _X          = ndX,
    y           = ndY,
    _Z          = ndZ,
    L           = 0,
    pls         = false,
    centerData  = false,
    scaleData   = true,
    closed_form = true,
    fitalg      = 2
  )

  val ndRsq =
    val yMean = (0 until mY.rows).map(mY(_, 0)).sum / mY.rows
    val ssTot = (0 until mY.rows).map(i => math.pow(mY(i,0) - yMean, 2)).sum
    val ssRes = (0 until mY.rows).map(i => math.pow(mY(i,0) - ndResult.y_hat(i, 0), 2)).sum
    if ssTot == 0.0 then 0.0 else 1.0 - ssRes / ssTot

  val ndVsMatDiff = (0 until mKpYhat.rows).map(i =>
    math.abs(ndResult.y_hat(i, 0) - result.y_hat(i, 0))
  ).fold(0.0)(math.max)

  println(f"NDArray R-squared: $ndRsq%.6f  (Matrix was: $rSquared%.6f)")
  println(f"NDArray vs Matrix yhat max diff: $ndVsMatDiff%.2e")
  if ndVsMatDiff < tol then
    println("SUCCESS: NDArray and Matrix implementations agree.")
  else
    println("FAILURE: NDArray and Matrix implementations disagree.")
    sys.exit(1)
